    public ContractDetailForm(String ID) {
        contractID = ID;
        JOptionPane.showMessageDialog(rootPane,ID);
        initComponents();
        ContractIDTextField.setText(ID);
        Connection conn = getConnection();
        BranchComboBox.removeAllItems();
        String detailQRY = "SELECT * FROM HOPDONG WHERE MAHD = ?";
        String branchsQRY = "SELECT MACN FROM CHINHANH WHERE MAHD = ?";
        try{
            PreparedStatement ppState = conn.prepareStatement(detailQRY);          
            ppState.setString(1,ID);
            rs = ppState.executeQuery();
            if(rs.next()){
                DeputyTextField.setText(rs.getString("DAIDIENHD"));
                QuantityTextField.setText(Integer.toString(rs.getInt("SOCHINHANH")));
                CreateDateTextField.setText(rs.getDate("NGAYLAP").toString());
                ExpTextField.setText(rs.getDate("NGAYKETTHUC").toString());
            }
            
            PreparedStatement ppS = conn.prepareCall(branchsQRY);
            ppS.setString(1,ID);
            branchRS = ppS.executeQuery();
            String[] branchs;
            while(branchRS.next()){
                BranchComboBox.addItem(branchRS.getString("MACN"));
            }
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane,e);
        }
    }

